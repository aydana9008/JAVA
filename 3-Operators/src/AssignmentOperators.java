
public class AssignmentOperators {

	public static void main(String[] args) {
		
		int i1=100;
		System.out.println(i1);
		
		i1+=1;   // 101
		System.out.println(i1);
		
		i1-=2;  // 99
		System.out.println(i1);
		
		i1*=3; // 297
		System.out.println(i1);
		
		i1/=4; // 74
		System.out.println(i1);
		

	}

}
